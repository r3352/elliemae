    public static AclTriState GetDefaultBrokerAccess(AclFeature feature)
    {
      lock (typeof (FeatureSets))
      {
        if (FeatureSets.defaultBrokerAccess == null)
        {
          FeatureSets.defaultBrokerAccess = new Dictionary<AclFeature, AclTriState>();
          foreach (AclFeature key in FeatureSets.All)
            FeatureSets.defaultBrokerAccess[key] = Array.IndexOf<AclFeature>(FeatureSets.BrokerFeatures, key) < 0 ? (Array.IndexOf<AclFeature>(FeatureSets.BankerOnlyFeatures, key) < 0 ? AclTriState.Unspecified : AclTriState.False) : AclTriState.Unspecified;
        }
      }
      return FeatureSets.defaultBrokerAccess.ContainsKey(feature) ? FeatureSets.defaultBrokerAccess[feature] : AclTriState.Unspecified;
    }

